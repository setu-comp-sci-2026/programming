# Exercise 4 - Combined arithmetic operators
num1 = 50
num2 = 120
num3 = 180

# Division on rows
print(num1, num2, num3, num2)
print(num1, num2 / 2, num3, num2 / 2)
print(num1, num2 / 3, num3, num2 / 3)
print(num1, num2 / 4, num3, num2 / 4)

# Using num1 and num3 with mixed operators
print(num1, num2, num1, num2 / 4)
print(num3, num2, num3, num2 / 4)
