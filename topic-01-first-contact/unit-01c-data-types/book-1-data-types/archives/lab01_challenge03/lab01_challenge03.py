# Challenge 3 - Two sets of coordinates, offset by 100
xCoord = 300
yCoord = 300

# First set - original values
print("Set 1:")
print("  xCoord:", xCoord)
print("  yCoord:", yCoord)
print("  xCoord + yCoord:", xCoord + yCoord)

# Second set - offset by 100
print("Set 2 (offset by 100):")
print("  xCoord - 100:", xCoord - 100)
print("  yCoord - 100:", yCoord - 100)
print("  (xCoord - 100) + (yCoord - 100):", (xCoord - 100) + (yCoord - 100))
