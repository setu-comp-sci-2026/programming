# Challenge 2 - Derived coordinates using division
xCoord = 300
yCoord = 300

newX = xCoord / 2
newY = yCoord / 4

print("Original xCoord:", xCoord, "  New xCoord:", newX)
print("Original yCoord:", yCoord, "  New yCoord:", newY)

# All four operators on the derived values
print("newX + newY =", newX + newY)
print("newX - newY =", newX - newY)
print("newX * newY =", newX * newY)
print("newX / newY =", newX / newY)
